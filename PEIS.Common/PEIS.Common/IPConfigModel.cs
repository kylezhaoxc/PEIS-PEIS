using System;

namespace PEIS.Common
{
	public class IPConfigModel
	{
		private string _GUID = string.Empty;

		private string _BeginIP = string.Empty;

		private string _EndIP = string.Empty;

		private string _DepartName = string.Empty;

		private string _DepartCode = string.Empty;

		private string _Enable = "1";

		private string _DefaultHeaderCode = "11";

