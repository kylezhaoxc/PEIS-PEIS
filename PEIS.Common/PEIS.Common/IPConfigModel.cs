			get
			{
				return this._GUID;
			}
			set
			{
				this._GUID = value;
			}
		}

		public string BeginIP
		{
			get
			{
				return this._BeginIP;
			}
			set
			{
				this._BeginIP = value;
			}
		}

		public string EndIP
		{
			get
			{
				return this._EndIP;
			}
			set
			{
				this._EndIP = value;
			}
		}

		public string DepartName
		{
			get
			{
				return this._DepartName;
			}
			set
			{
				this._DepartName = value;
			}
		}

		public string DepartCode
		{
			get
			{
				return this._DepartCode;
			}
			set
			{
				this._DepartCode = value;
			}
		}

		public string Enable
		{
			get
			{
				return this._Enable;
			}
			set
			{
				this._Enable = value;
			}
		}

		public string DefaultHeaderCode
		{
			get
			{
				return this._DefaultHeaderCode;
			}
			set
			{
				this._DefaultHeaderCode = value;
			}
		}
	}
}
